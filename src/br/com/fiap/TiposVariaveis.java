package br.com.fiap;

public class TiposVariaveis {
    public static void main(String[] args) {
        // Tipos Primitivos
        char sexo = 'M';
        byte idade = 19;
        short codigo = 22080;
        int alunos = 50, classes = 11;
        long brasileiros = 216535748;
        float media = 8.7f;
        double dolar = 5.75;
        boolean alternativa = false;

        System.out.println("Sexo:\t" + sexo);
        System.out.println("Idade:\t" + idade);
        System.out.println("Código:\t" + codigo);
        System.out.println("Média:\t" + media);

        // \t -> tabulação
        // \n -> pula linha
    }
}
