package br.com.fiap;

public class PrimeiraClasse {
    public static void main(String[] args) {
        System.out.println("Hello world!");
    }

    /*
    Encapsulamento: separação dos aspectos externos de um objeto,
    acessíveis por outros objetos, e aspectos internos, acessíveis
    apenas dentro do próprio objeto.

    Modularidade: construir elementos que sejam autônomos. Garante
    reutilização.

    Classe: molde que define características (atributos) e comportamentos
    (métodos) de um objeto.

    Objeto: instância de uma classe.

    Herança: quando uma classe pai (superclasse) é herdada por subclasses.
    ex: classes Cachorro e Gato herdam da classe Animal.

    Polimorfismo:
    - Sobrescrita: método com mesmo nome, mas com funcionalidade diferente.
    - Sobrecarga: quando existem dois ou mais métodos com o mesmo nome, na
    mesma classe, mas com assinaturas diferentes. O método a ser usado é
    escolhido de acordo com o números de parâmetros passados.
    */
}
